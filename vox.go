package main

import (
	"github.com/go-gl/gl/v4.5-core/gl"
	"github.com/go-gl/mathgl/mgl32"
)

type vox struct {
	x            float32
	y            float32
	z            float32
	model        mgl32.Mat4
	modelUniform int32
}

func CreateVox(x, y, z float32, program uint32) (v *vox) {
	v = &vox{x: x, y: y, z: z}
	v.model = mgl32.Ident4()
	v.modelUniform = gl.GetUniformLocation(program, gl.Str("model\x00"))
	gl.UniformMatrix4fv(v.modelUniform, 1, false, &v.model[0])
	return
}

func (v *vox) Render() {

	v.model = mgl32.Translate3D(v.x, v.y, v.z)
	gl.UniformMatrix4fv(v.modelUniform, 1, false, &v.model[0])
	gl.DrawArrays(gl.TRIANGLES, 0, 6*2*3)

}

